package constantesoperadores;

public class operadores {

	public static void main(String[] args) {
		
		int numero1 = 10;
		
		int numero2 = 10;
		
		//SUMA
		
		int resultadoSuma;
		
		resultadoSuma = numero1 + numero2;
		
		System.out.println("El resultado de la suma es de : " + resultadoSuma);
		
		
		//MULTIPLICACIÓN
		
		int resultadoMultiplicación;
		
		resultadoMultiplicación = numero1 * numero2;
		
		System.out.println("El resultado de la multiplicación es de : " + resultadoMultiplicación); 
		
		
		//RESTA
		
		int resultadoResta;
		
		resultadoResta = numero1 - numero2;
		
		System.out.println("El resultado de la resta es de : " + resultadoResta);
		
		//DIVISION
		
		int resultadoDivisión;
		
		resultadoDivisión = numero1 / numero2;
		
		System.out.println("El resultado de la division es de : " + resultadoDivisión);
		
		
		//INCREMENTAL
		
		int numero3 = 6;
		
		numero3++;
		
		System.out.println(numero3);
		
		
		//Variables
		
		double precio;
		
		precio = 2.22;
		
		System.out.println(precio); 
				
				
		
	}

}
